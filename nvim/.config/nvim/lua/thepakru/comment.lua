-- nvim.comment 

require('Comment').setup()

-- Mappings 

-- NORMAL MODE
-- `gcc` - toggle the current line using linewise comment
-- `gbc` - toggle the current line using blockwise comment 
-- `[count]gcc - toggles the number of lines 
-- `[count]]gbc - toggles the number of lines in blockwise 

-- VISUAL MODE
-- `gc`: toggle linewise comment 
-- `gb`: toggle lockwise comment 
-- 

-- EXTRA MAPPINGS 

-- NORMAL MODE
-- `gco`: insert comment to the next line and enters insert mode
-- `gcO`: insert comment to the previous line and enters insert mode
-- `gcA`: insert comment to the end of the current line and enters insert mode



